package com.babyraising.friendstation.bean;

public class VerifyBean {
    private String certAvatar;
    private String certRemark;
    private String statusCert;

    public String getCertAvatar() {
        return certAvatar;
    }

    public void setCertAvatar(String certAvatar) {
        this.certAvatar = certAvatar;
    }

    public String getCertRemark() {
        return certRemark;
    }

    public void setCertRemark(String certRemark) {
        this.certRemark = certRemark;
    }

    public String getStatusCert() {
        return statusCert;
    }

    public void setStatusCert(String statusCert) {
        this.statusCert = statusCert;
    }
}
